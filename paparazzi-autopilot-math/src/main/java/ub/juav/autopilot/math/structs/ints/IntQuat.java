public class IntQuat {
    private int qi;
    private int qx;
    private int qy;
    private int qz;

    public int getQi() {
        return qi;
    }

    public void setQi(int qi) {
        this.qi = qi;
    }

    public int getQx() {
        return qx;
    }

    public void setQx(int qx) {
        this.qx = qx;
    }

    public int getQy() {
        return qy;
    }

    public void setQy(int qy) {
        this.qy = qy;
    }

    public int getQz() {
        return qz;
    }

    public void setQz(int qz) {
        this.qz = qz;
    }
}
