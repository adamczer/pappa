package ub.juav.autopilot.math.structs.doubles;

/**
 * Rotation quaternion
 */
public class DoubleQuat {
    private double qi;
    private double qx;
    private double qy;
    private double qz;

    public double getQi() {
        return qi;
    }

    public void setQi(double qi) {
        this.qi = qi;
    }

    public double getQx() {
        return qx;
    }

    public void setQx(double qx) {
        this.qx = qx;
    }

    public double getQy() {
        return qy;
    }

    public void setQy(double qy) {
        this.qy = qy;
    }

    public double getQz() {
        return qz;
    }

    public void setQz(double qz) {
        this.qz = qz;
    }
}
