package ub.juav.autopilot.math.structs.doubles;

/**
 * Angular rates
 * units are in radians
 */
public class DoubleRates {
    private double p;
    private double q;
    private double r;

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }

    public double getQ() {
        return q;
    }

    public void setQ(double q) {
        this.q = q;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }
}
